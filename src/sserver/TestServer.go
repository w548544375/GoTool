func recvBuff(connection net.Conn) {
	buff := make([]byte, 256)
	for {
		n, err := connection.Read(buff[0:])
		if err != nil {
			connection.Close()
			fmt.Println("客户端断开连接")

			break
		}
		for i := 0; i < n; i++ {
			fmt.Printf("%02X ", buff[i])
		}
		fmt.Println()
	}
}
